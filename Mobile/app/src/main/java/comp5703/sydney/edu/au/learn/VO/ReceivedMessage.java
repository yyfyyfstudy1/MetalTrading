package comp5703.sydney.edu.au.learn.VO;

public class ReceivedMessage {
    private Integer from;
    private String text;


    public Integer getFrom() {
        return from;
    }

    public void setFrom(Integer from) {
        this.from = from;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
